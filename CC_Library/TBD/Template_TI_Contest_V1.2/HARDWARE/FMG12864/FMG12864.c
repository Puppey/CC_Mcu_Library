//***************************************************************************SPI4
void Write_uc1701_COM(u8 COMDADA_4SPI)
{
	u8 bit8;
	RS=0;
//	CS0=0;
	for(bit8=0;bit8<8;bit8++)
	{
		SCL=0;
		if((COMDADA_4SPI&0x80)==0x80)
		SDI=1;
		else		
		SDI=0;
//		SCL=0;
		SCL=1;
		COMDADA_4SPI=(COMDADA_4SPI<<1);

	}
  //  CS1=1;
			
}

void Write_uc1701_DISPLAY_DATA_serial_4SPI(u8 DADA_4SPI)
{   
	u8 bit8;
	RS=1;
//	CS0=0;				

	for(bit8=0;bit8<8;bit8++)
	{
		SCL=0;
		if((DADA_4SPI&0x80)==0x80)
		SDI=1;
		else
		SDI=0;
		SCL=1;
		DADA_4SPI=(DADA_4SPI<<1);

	}
//	CS1=1;
}

void HDReset()
{
	RSTB=0;
	delay_ms(2);
	RSTB=1;
	delay_ms(4);
}      	


void Set_XY(u16 x,u16 y)
{
	if(y>3)
		y-=4;
	else
		y+=4;
	Write_uc1701_COM(0xb0|y);
	Write_uc1701_COM(0x10|((x&0xf0)>>4));
	Write_uc1701_COM(0x01|(x&0x0f));
}

void LCD_Write_6x8(u8 c)
{
	u8 line;
	c-=32;
	for(line=0;line<6;line++)
		Write_uc1701_DISPLAY_DATA_serial_4SPI(F6x8[c][line]);
}

void LCD_Write_8x16(u8 x,u8 y,u8 c)
{
	u8 line;
	Set_XY(x,y);
	c-=32;
	for(line=0;line<8;line++)
		Write_uc1701_DISPLAY_DATA_serial_4SPI(F8X16[c][line]);
	Set_XY(x,y+1);
	for(line=0;line<8;line++)
		Write_uc1701_DISPLAY_DATA_serial_4SPI(F8X16[c][line+8]);
}

void LCD_Write_Chinese(u8 x,u8 y,u8 c)
{
	u8 line;
	Set_XY(x,y);
	for(line=0;line<16;line++)
		Write_uc1701_DISPLAY_DATA_serial_4SPI(Hzk[c][line]);
	Set_XY(x,y+1);
	for(line=0;line<16;line++)
		Write_uc1701_DISPLAY_DATA_serial_4SPI(Hzk[c][line+16]);
}
